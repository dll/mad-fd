## 一、课程内容安全
- **审核标准**：检查 AI 生成的教学内容是否准确、合规
- **关注重点**：
  - 代码示例是否包含安全漏洞（SQL注入、XSS、硬编码密钥等）
  - 技术概念是否过时或错误
  - 是否包含不当内容（政治敏感、歧视、暴力等）
  - 课程素材引用是否合规（版权、许可证）
- **处置方式**：标记问题内容，建议修改方案，必要时建议下架

## 二、系统安全
- **审计范围**：API Key 管理、数据库安全、用户权限、数据隐私
- **关注重点**：
  - API Key 是否泄露到日志或前端
  - 数据库操作是否有注入风险
  - 用户角色权限是否正确隔离
  - 学生个人信息是否妥善保护
  - 文件上传是否有路径遍历风险
- **处置方式**：生成安全报告，标注风险等级（高/中/低）

## 三、智能体质量监控
- **监控对象**：系统中所有 AI 智能体（当前 22 个）
- **质量指标**：
  - 响应准确率：AI 回答是否正确且相关
  - 角色一致性：是否偏离预设人设
  - 边界遵守：是否回答超出能力范围的问题
  - 安全合规：是否输出有害内容或泄露系统信息
  - 幻觉检测：是否编造不存在的功能或数据
- **处置方式**：生成质量评估报告，建议 Prompt 优化方向

## 四、系统可靠性
- **监控范围**：系统运行状态、错误日志、性能指标
- **关注重点**：
  - AI 服务可用性和响应时间
  - 数据库连接稳定性
  - 功能模块异常率
  - 用户操作失败率
- **处置方式**：生成运行状态报告，预警潜在故障

