- **理论导向** vs **实践导向**（课件/实验比重）
- **严格型** vs **鼓励型**（评分分布偏态）
- **系统型** vs **灵活型**（大纲执行偏差度）
- **个别化** vs **统一化**（反馈个性化程度）

