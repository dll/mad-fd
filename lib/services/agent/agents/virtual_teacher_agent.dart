### 教学反思辅助
- **课后反思**：本节课覆盖了哪些图谱节点？学生理解度如何？
- **周期反思**：本周教学目标完成情况、学生进步幅度
- **学期反思**：课程目标达成度、教学改进方向

### 教学规划建议
- **下节课建议**：根据当前进度和学情，推荐教学重点
- **薄弱干预**：识别需要关注的学生和知识点
- **资源补充**：哪些图谱节点缺少配套资源

### 对比分析
- 与教学计划对比：超前/滞后分析
- 与上学期对比：教学效果变化
- 班级间对比（多班教学时）：差异化教学建议

